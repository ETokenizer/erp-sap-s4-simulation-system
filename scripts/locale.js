let currentLocale = 'zh-CN';

const Locale = {

  /**
   * 获取当前语言对应的文本
   * @param {string} key - 字典 key
   * @returns {string}
   */
  t(key) {
    const dict = LOCALES[currentLocale];
    if (dict && dict[key] !== undefined) {
      return dict[key];
    }
    // Fallback: try zh-CN, then return the key itself
    console.warn('[Locale] Missing key:', key, 'in', currentLocale);
    const zh = LOCALES['zh-CN'];
    if (zh && zh[key] !== undefined) return zh[key];
    return key;
  },

  /**
   * 设置语言
   * @param {string} lang - 'zh-CN' 或 'en-US'
   */
  set(lang) {
    currentLocale = lang;
    localStorage.setItem('erpSimulationLocale', lang);
  },

  /**
   * 获取当前语言
   * @returns {string}
   */
  current() {
    return currentLocale;
  },

  /**
   * 初始化（从 localStorage 读取，默认中文）
   */
  init() {
    currentLocale = localStorage.getItem('erpSimulationLocale') || 'zh-CN';
  },

  /**
   * 切换语言并刷新页面
   */
  toggle() {
    this.set(this.current() === 'zh-CN' ? 'en-US' : 'zh-CN');
    window.location.reload();
  },

  /**
   * 批量替换所有 [data-i18n] 元素的文本
   */
  applyToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.t(key);
      // 如果元素有 placeholder 属性，替换 placeholder
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        if (el.hasAttribute('placeholder') && el.getAttribute('data-i18n-placeholder')) {
          el.placeholder = text;
        } else {
          el.value = text;
        }
      } else if (el.tagName === 'OPTION') {
        el.textContent = text;
      } else {
        el.textContent = text;
      }
    });

    // 替换 [data-i18n-title] 元素的 title 属性
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      el.title = this.t(key);
    });

    // 替换 [data-i18n-placeholder] 元素的 placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = this.t(key);
    });

    // 更新语言切换按钮标签
    const langLabel = document.getElementById('langLabel');
    if (langLabel) {
      langLabel.textContent = currentLocale === 'zh-CN' ? 'EN' : '中文';
    }
  }
};

// 初始化
Locale.init();

// DOM 加载后自动替换
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => Locale.applyToDOM());
} else {
  Locale.applyToDOM();
}

window.Locale = Locale;
console.log('[Locale] Initialized:', currentLocale);
